def skip_path(p: Path) -> bool:
    for part in p.parts:
        if part in SKIP_DIRS or part.startswith("."):
            return True
    return False


def main() -> None:
    changed: list[str] = []
    for path in ROOT.rglob("*"):
        if not path.is_file() or skip_path(path.relative_to(ROOT)):
            continue
        if path.suffix not in EXT_OK:
            continue
        if "package-lock" in path.name:
            continue
        try:
            text = path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        orig = text
        for a, b in PHRASES:
            text = text.replace(a, b)
        text = re.sub(r"(?<![A-Za-z])圣经(?![A-Za-z])", "本书锦囊", text)
        while "本书本书锦囊" in text:
            text = text.replace("本书本书锦囊", "本书锦囊")
        if text != orig:
            path.write_text(text, encoding="utf-8")
            changed.append(str(path.relative_to(ROOT)))

    print(f"changed {len(changed)} files")
    for c in sorted(changed):
        print(c)


if __name__ == "__main__":
    main()
